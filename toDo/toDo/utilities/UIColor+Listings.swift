import UIKit

extension UIColor {

    
    static func darkGreyTextColor() -> UIColor {
        return UIColor(red:111.0 / 255.0,
                       green:124.0 / 255.0 ,
                       blue:141.0 / 255.0,
                       alpha:1.0)
    }
    
    static func themeTextColor() -> UIColor {
        return UIColor(red:131/255.0,
                       green:131/255.0 ,
                       blue:131/255.0,
                       alpha:1.0)
    }

    static func cellSeparatorDarkColor() -> UIColor {
        return UIColor(red:221/255.0,
                       green:221/255.0 ,
                       blue:221/255.0,
                       alpha:1.0)
    }

    static func themeBackRoundColor() -> UIColor {
        return UIColor(red:84/255.0,
                       green:118/255.0 ,
                       blue:80/255.0,
                       alpha:1.0)
    }

}
